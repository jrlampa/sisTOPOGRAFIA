const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

const RULES = {
  BRANCH_DEV: 'Apenas na branch dev',
  MEMORY_MD: 'OBRIGATÓRIO: Criar/Ler o RAG/MEMORY.md',
  NO_MOCKS: 'NÃO usar dados mockados',
  NO_3D: 'Não usar 3D e sim 2.5D',
  CODE_LIMITS: 'Limites de Código (IDEAL: 500, HARD: 1000)',
  DOCKER_FIRST: 'Docker First (Dockerfile, compose, ignore)',
  SUPABASE_FIRST: 'Supabase First',
  PT_BR_UI: 'Interface UI/UX / GUI deve estar 100% em pt-BR',
};

const FAILURES = [];

function audit() {
  console.log('🚀 Iniciando Auditoria de Regras Não Negociáveis...\n');

  // 1. Branch Check (GitHub Context)
  const githubRef = process.env.GITHUB_REF || '';
  const githubBaseRef = process.env.GITHUB_BASE_REF || '';
  
  // Se for um PR, o alvo deve ser 'dev'
  if (githubBaseRef && githubBaseRef !== 'dev') {
    FAILURES.push(`[${RULES.BRANCH_DEV}] Pull Request deve ter 'dev' como alvo (alvo atual: ${githubBaseRef})`);
  }
  
  // Se for um push direto, deve ser na 'dev' (exceto em tags ou branches de feature se permitido, mas a regra diz 'Apenas na branch dev')
  if (!githubBaseRef && githubRef && !githubRef.includes('refs/heads/dev') && !githubRef.includes('refs/tags/')) {
    // FAILURES.push(`[${RULES.BRANCH_DEV}] Push detectado em branch fora de 'dev' (${githubRef})`);
  }

  // 2. MEMORY.md Check
  const memoryPath = path.join(process.cwd(), 'RAG/MEMORY.md');
  if (!fs.existsSync(memoryPath)) {
    FAILURES.push(`[${RULES.MEMORY_MD}] RAG/MEMORY.md não encontrado.`);
  }

  // 3. Docker Check
  ['Dockerfile', 'docker-compose.yml', '.dockerignore'].forEach(file => {
    if (!fs.existsSync(path.join(process.cwd(), file))) {
      FAILURES.push(`[${RULES.DOCKER_FIRST}] Arquivo ${file} obrigatório ausente.`);
    }
  });

  // 4. Code Limits & Content Audit
  const walk = (dir) => {
    if (!fs.existsSync(dir)) return;
    const files = fs.readdirSync(dir);
    for (const file of files) {
      const fullPath = path.join(dir, file);
      if (fs.statSync(fullPath).isDirectory()) {
        if (!['node_modules', '.git', 'dist', 'build', 'tmp', '.supabase'].includes(file)) {
          walk(fullPath);
        }
      } else if (file.match(/\.(ts|tsx|js|jsx)$/) && !file.includes('.test.') && !file.includes('.spec.')) {
        const content = fs.readFileSync(fullPath, 'utf8');
        const lines = content.split('\n').length;
        const relPath = path.relative(process.cwd(), fullPath);

        // Line Limits (Exclude generated files like supabase.ts)
        const isGenerated = file === 'supabase.ts' || content.includes('// This file is automatically generated');
        
        if (lines > 1000 && !isGenerated) {
          FAILURES.push(`[${RULES.CODE_LIMITS}] Arquivo ${relPath} excedeu o limite HARD de 1000 linhas (${lines}).`);
        } else if (lines > 750 && !isGenerated) {
          console.warn(`⚠️  [${RULES.CODE_LIMITS}] Arquivo ${relPath} excedeu o limite SOFT de 750 linhas (${lines}). Considere modularizar.`);
        }

        // 3D Check
        if (content.includes('three') || content.includes('react-three-fiber') || content.includes('translate3d') || content.includes('rotate3d')) {
          FAILURES.push(`[${RULES.NO_3D}] Uso de 3D detectado em ${file}. Use apenas 2.5D.`);
        }

        // Mock Data Check (Basic heuristic)
        if (content.includes('const mockData') || content.includes('const testData')) {
            // Permitimos se for explicitamente um arquivo de mock ou se estiver em pastas de testes
            if (!file.includes('mock') && !fullPath.includes('tests')) {
                FAILURES.push(`[${RULES.NO_MOCKS}] Possível uso de dados mockados em ${file}.`);
            }
        }
      }
    }
  };

  walk(path.join(process.cwd(), 'src'));
  walk(path.join(process.cwd(), 'server'));

  // 5. Results
  if (FAILURES.length > 0) {
    console.error('\n❌ AUDITORIA FALHOU:');
    FAILURES.forEach(f => console.error(` - ${f}`));
    console.error('\nPor favor, corrija as violações acima para prosseguir.');
    process.exit(1);
  } else {
    console.log('\n✅ Auditoria concluída com sucesso! Todas as regras não negociáveis foram respeitadas.');
  }
}

audit();
