// Heuristic: Read the text summary if generated by vitest/jest
async function main() {
  console.log("📊 Consolidando métricas de cobertura global...");
  
  // Heuristic: Use the coverage-summary.json if configured in vitest.config.ts
  const BE_SUMMARY = "sisrua_unified/coverage/backend/coverage-summary.json";
  const FE_SUMMARY = "sisrua_unified/coverage/frontend-unit/coverage-summary.json";


  if (!existsSync(BE_SUMMARY) || !existsSync(FE_SUMMARY)) {
    console.warn("⚠️  Reports de sumário não encontrados. Execute os testes com --coverage primeiro.");
    return;
  }

  const be = JSON.parse(readFileSync(BE_SUMMARY, "utf8")).total;
  const fe = JSON.parse(readFileSync(FE_SUMMARY, "utf8")).total;

  const total = {
    lines: (be.lines.pct + fe.lines.pct) / 2,
    statements: (be.statements.pct + fe.statements.pct) / 2,
    functions: (be.functions.pct + fe.functions.pct) / 2,
    branches: (be.branches.pct + fe.branches.pct) / 2,
  };

  console.log("\n📈 Resultados Consolidados:");
  console.log(`- Linhas: ${total.lines.toFixed(2)}%`);
  console.log(`- Funções: ${total.functions.toFixed(2)}%`);
  console.log(`- Statements: ${total.statements.toFixed(2)}%`);
  console.log(`- Branches: ${total.branches.toFixed(2)}%`);

  const globalAvg = (total.lines + total.functions + total.statements + total.branches) / 4;
  console.log(`\n💎 MÉDIA GLOBAL: ${globalAvg.toFixed(2)}%`);

  if (globalAvg < 80) {
    console.error(`\n❌ FALHA: Cobertura global (${globalAvg.toFixed(2)}%) abaixo da meta de 80%.`);
    process.exit(1);
  } else {
    console.log("\n✅ SUCESSO: Meta de cobertura atingida.");
  }
}

main().catch(console.error);
